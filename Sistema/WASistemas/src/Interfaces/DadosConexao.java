    private void btnTestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestarActionPerformed
        conexao = new Conexao();
        
        String retorno = conexao.getTestConection(txtServidor.getText(), txtUsuario.getText(), String.valueOf(txtSenha.getPassword()), txtBanco.getText());
        
        if (retorno.equals("")){
            JOptionPane.showMessageDialog(this, "Conectado com Sucesso!");
        }else{
            JOptionPane.showMessageDialog(this, "Problemas na conexão! /n" + retorno);
        }
    }//GEN-LAST:event_btnTestarActionPerformed

